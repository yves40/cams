VERSION="startmongo.sh v 1.01, "
VERSIONDATE="Feb 09 2019 "
LOG="/tmp/mongo.log"
#--------------------------------------------------------------------------------
# Logger
#--------------------------------------------------------------------------------
log()
{
        echo "`date '+%Y-%m-%d %H:%M:%S'` : $VERSION $1" >> $LOG
        echo "`date '+%Y-%m-%d %H:%M:%S'` : $1"
}
#---------------------------------------------------------------------------------------
#   Node processes start
#---------------------------------------------------------------------------------------
echo
echo
log 'Starting mongodb'
echo
rm -f /data/db/mongod.lock
log "nohup mongod --port 4100 --quiet"
nohup mongod --port 4100 --quiet >/dev/null 2>&1 &
